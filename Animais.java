package br.com.generation.animais;

public class Animais {
	private String nome;
	private int idade;
	
	public String somAnimal(String som) {
		return som;
		
	}
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	

}
